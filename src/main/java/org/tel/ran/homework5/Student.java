package org.tel.ran.homework5;

public class Student {
    private String name;
    private String avgMark;

    public Student(String name, String avgMark) {
        this.name = name;
        this.avgMark = avgMark;
    }

    public String getName() {
        return name;
    }

    public String getAvgMark() {
        return avgMark;
    }
}
